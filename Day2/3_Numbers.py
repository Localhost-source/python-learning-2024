# Numbers are immutable datatype in python
# There are three types of numbers
# Integer
# Float
# Complex

# Built in function for integer is int()
# float() for float type
# complex() for complex type


# Int
a = 34
b = -12
c = 0

# Float
d = 1.9
e = -2.2
f = 2e4
print(f)  # 20000.0
g = 2e-2
print(g)  # 0.02

data = int(d)
print(data)  # 1


# Complex type
a = 3 + 2j
b = -4j
c = 6j-2

print(c)